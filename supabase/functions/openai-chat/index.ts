// supabase/functions/openai-chat/index.ts
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface ChatRequest {
  message: string;
  systemPrompt?: string;
  model?: string;
  temperature?: number;
  topP?: number;
  maxTokens?: number;
  language?: string;
  tone?: string;
}

interface Product {
  name: string;
  price: number;
  description?: string;
  category?: string;
  sku?: string;
  image_url?: string;
  external_id?: string;
  stock_quantity?: number;
}

interface DocumentationFile {
  name: string;
  content: string;
  file_type: string;
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    // Initialize Supabase client
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_ANON_KEY') ?? '',
    )

    // Get OpenAI API key from environment
    const openaiApiKey = Deno.env.get('OPENAI_API_KEY')
    if (!openaiApiKey) {
      return new Response(
        JSON.stringify({ error: 'OpenAI API key not configured' }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      )
    }

    // Parse request body
    const { message, systemPrompt, model = 'gpt-4o-mini', temperature = 0.7, topP = 0.9, maxTokens = 2048, language = 'es', tone = 'friendly' }: ChatRequest = await req.json()

    if (!message) {
      return new Response(
        JSON.stringify({ error: 'Message is required' }),
        { 
          status: 400, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      )
    }

    // Get products from Supabase
    const { data: products, error: productsError } = await supabaseClient
      .from('product_catalog')
      .select('name, price, description, category, sku, image_url, external_id, stock_quantity')
      .eq('status', 'active')
      .limit(10000) // Asegurar que obtenemos todos los productos

    if (productsError) {
      console.error('Error fetching products:', productsError)
    }

    // Log products data for debugging
    console.log('üîç PRODUCTOS OBTENIDOS DE LA BASE DE DATOS:');
    console.log('üìä Total productos:', products?.length || 0);
    if (products && products.length > 0) {
      console.log('üìã Primeros 3 productos:');
      products.slice(0, 3).forEach((p, i) => {
        console.log(`Producto ${i + 1}:`, {
          name: p.name,
          price: p.price,
          image_url: p.image_url,
          external_id: p.external_id,
          category: p.category
        });
      });
      
      // Buscar espec√≠ficamente el producto Heavy Wheel Kit
      const heavyWheelKit = products.find(p => p.name.toLowerCase().includes('heavy wheel kit'));
      if (heavyWheelKit) {
        console.log('üéØ HEAVY WHEEL KIT ENCONTRADO:', heavyWheelKit);
      } else {
        console.log('‚ùå Heavy Wheel Kit NO encontrado en la base de datos');
      }
    }

    // Get documentation from Supabase
    const { data: docs, error: docsError } = await supabaseClient
      .from('documentation_files')
      .select('name, content, file_type')
      .eq('status', 'ready')

    if (docsError) {
      console.error('Error fetching documentation:', docsError)
    }

    // Build catalog info for prompt with smart product selection
    const maxProducts = 50;
    let selectedProducts = products ? products.slice(0, maxProducts) : [];
    
    // If user asks for a specific product, try to find it
    const messageLower = message.toLowerCase();
    const specificProduct = products?.find(p => 
      p.name.toLowerCase().includes(messageLower) || 
      messageLower.includes(p.name.toLowerCase())
    );
    
    // If specific product found, include it in the list
    if (specificProduct && !selectedProducts.find(p => p.name === specificProduct.name)) {
      selectedProducts = [specificProduct, ...selectedProducts.slice(0, maxProducts - 1)];
    }
    
    const catalogInfo = products && products.length > 0 
      ? `üè™ CAT√ÅLOGO DE PRODUCTOS (${products.length} productos total, mostrando ${selectedProducts.length} productos relevantes):
${selectedProducts.map((p: Product) => {
  let productInfo = `üì¶ ${p.name}`;
  productInfo += `\n   üí∞ Precio: ${p.price}‚Ç¨`;
  productInfo += `\n   üìù Descripci√≥n: ${p.description || 'Sin descripci√≥n'}`;
  productInfo += `\n   üè∑Ô∏è Categor√≠a: ${p.category || 'Sin categor√≠a'}`;
  productInfo += `\n   üî¢ SKU: ${p.sku || 'N/A'}`;
  
  if (p.image_url) {
    productInfo += `\n   üñºÔ∏è Imagen: ${p.image_url}`;
  }
  
  if (p.external_id) {
    productInfo += `\n   üîó URL Producto: ${p.external_id}`;
  }
  
  if (p.stock_quantity !== undefined) {
    productInfo += `\n   üìä Stock: ${p.stock_quantity}`;
  }
  
  return productInfo;
}).join('\n\n')}

üìÇ CATEGOR√çAS DISPONIBLES:
${[...new Set(products.map((p: Product) => p.category).filter(Boolean))].join(', ')}

‚ö†Ô∏è REGLA CR√çTICA: Si el usuario pregunta por un producto espec√≠fico y no aparece en la lista de arriba, busca en TODOS los ${products.length} productos disponibles usando palabras clave del nombre del producto. SIEMPRE prioriza la informaci√≥n del cat√°logo sobre cualquier otra fuente.`
      : 'No hay productos disponibles en el cat√°logo.'

    // Build documentation info for prompt (limit content to avoid token limit)
    const documentationInfo = docs && docs.length > 0
      ? `DOCUMENTACI√ìN ADICIONAL DISPONIBLE (${docs.length} archivos):
${docs.map((doc: DocumentationFile) => 
  `- ${doc.name} (${doc.file_type.toUpperCase()}): ${doc.content.substring(0, 200)}...`
).join('\n')}`
      : 'No hay documentaci√≥n adicional disponible.'

    // Add web search capability note
    const webSearchInfo = `INFORMACI√ìN WEB DISPONIBLE:
- Puedes acceder a informaci√≥n actualizada de la web para complementar las respuestas
- Usa esta informaci√≥n para datos actuales, precios de mercado, especificaciones t√©cnicas, etc.
- Combina la informaci√≥n del cat√°logo con datos web para dar respuestas m√°s completas`

    // Build enhanced system prompt - PROMPT PRINCIPAL √öNICO
    const enhancedSystemPrompt = `${systemPrompt || 'Eres un asistente especializado en ayudar a clientes a encontrar productos. Siempre s√© amable, directo y enf√≥cate en las necesidades del cliente. Proporciona recomendaciones basadas en el cat√°logo disponible.'}

${catalogInfo}

${documentationInfo}

${webSearchInfo}

INSTRUCCIONES CR√çTICAS DEL PROMPT PRINCIPAL:
1. REGLA DE PRIORIDAD ABSOLUTA: Si encuentras un producto en el cat√°logo, SIEMPRE muestra PRIMERO la informaci√≥n del cat√°logo
2. ANTES DE RESPONDER, revisa cuidadosamente TODA la informaci√≥n disponible:
   - ‚úÖ CAT√ÅLOGO: Revisa todos los productos disponibles (PRIORIDAD M√ÅXIMA)
   - ‚úÖ DOCUMENTACI√ìN: Consulta los archivos de documentaci√≥n
   - ‚úÖ WEB: Usa informaci√≥n web SOLO para complementar, NUNCA para reemplazar datos del cat√°logo
3. Si el usuario pregunta por un producto espec√≠fico:
   - Busca en la lista de productos mostrada arriba
   - Si lo encuentras en el cat√°logo, muestra SIEMPRE primero esa informaci√≥n
   - Si no lo encuentras, usa palabras clave para buscar productos similares
   - Si hay ${products.length} productos en total, es posible que el producto est√© disponible pero no aparezca en los primeros 50
4. Para b√∫squedas de productos:
   - Usa palabras clave del nombre del producto
   - Busca variaciones del nombre (ej: "noodle" para "Noodle Boxes")
   - Revisa categor√≠as relacionadas
5. GENERACI√ìN AUTOM√ÅTICA DE TARJETAS: 
   - El sistema autom√°ticamente genera una tarjeta visual cuando encuentra un producto
   - La tarjeta ya incluye: imagen, precio, descripci√≥n y bot√≥n de compra
   - NO repitas informaci√≥n que ya est√° en la tarjeta
6. RESPUESTA CONVERSACIONAL OBLIGATORIA:
   - SOLO da una respuesta conversacional sobre el producto
   - NO incluyas NUNCA: precio, descripci√≥n t√©cnica, SKU, categor√≠a, imagen o enlaces
   - NO uses formato de lista con guiones (-) o asteriscos (*)
   - NO uses encabezados con ### o ##
   - Habla de los beneficios, usos, caracter√≠sticas especiales
   - Sugiere casos de uso o combinaciones con otros productos
   - Pregunta si necesita m√°s informaci√≥n o productos relacionados
   - Mant√©n un tono amigable y √∫til
7. PROHIBIDO ABSOLUTO en tu respuesta:
   - Precio (ej: "49‚Ç¨", "Precio: 49‚Ç¨")
   - Descripci√≥n t√©cnica (ej: "Compatible con vasos y copas")
   - SKU o categor√≠a (ej: "SKU: 10/0046")
   - Imagen o enlaces (ej: "![imagen]", "[Ver m√°s]")
   - Formato de lista con guiones (-)
   - Encabezados con ### o ##
8. OBLIGATORIO en tu respuesta:
   - Beneficios del producto
   - Casos de uso pr√°cticos
   - Sugerencias de productos relacionados
   - Preguntas √∫tiles para el cliente
   - Solo texto conversacional en p√°rrafos
7. Si no encuentras informaci√≥n espec√≠fica, di exactamente qu√© has buscado y qu√© informaci√≥n tienes disponible
8. AL FINAL DE CADA RESPUESTA, el sistema a√±adir√° autom√°ticamente un disclaimer espec√≠fico indicando las fuentes exactas de tu respuesta
9. Mant√©n el tono: ${tone}
10. Responde en ${language === 'es' ? 'espa√±ol' : language === 'en' ? 'ingl√©s' : language}

IMPORTANTE: Este es el PROMPT PRINCIPAL √öNICO. Todas las instrucciones est√°n aqu√≠. No hay otros prompts adicionales.`

    // Log para verificar que el prompt se lee correctamente
    console.log('üîç PROMPT PRINCIPAL √öNICO ENVIADO A OPENAI:');
    console.log('üìù Longitud del prompt:', enhancedSystemPrompt.length, 'caracteres');
    console.log('üìã PROMPT PRINCIPAL DEL USUARIO (primeros 300 caracteres):', systemPrompt?.substring(0, 300) || 'NO HAY PROMPT PRINCIPAL');
    console.log('üìã INSTRUCCIONES CR√çTICAS A√ëADIDAS:', enhancedSystemPrompt.substring(systemPrompt?.length || 0, (systemPrompt?.length || 0) + 200));
    console.log('üìã √öltimos 200 caracteres:', enhancedSystemPrompt.substring(enhancedSystemPrompt.length - 200));
    console.log('‚úÖ PROMPT PRINCIPAL √öNICO COMPLETO ENVIADO A OPENAI - OPENAI LEER√Å ESTE PROMPT ANTES DE CONTESTAR');

    // Call OpenAI API
    const openaiResponse = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openaiApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: model,
        messages: [
          {
            role: 'system',
            content: enhancedSystemPrompt
          },
          {
            role: 'user',
            content: message
          }
        ],
        temperature: temperature,
        top_p: topP,
        max_tokens: maxTokens,
        stream: false
      })
    })

    if (!openaiResponse.ok) {
      const errorData = await openaiResponse.json()
      return new Response(
        JSON.stringify({ 
          error: `OpenAI API error: ${errorData.error?.message || openaiResponse.statusText}`,
          code: errorData.error?.code
        }),
        { 
          status: openaiResponse.status, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      )
    }

    const openaiData = await openaiResponse.json()
    
    // Apply tone to response and analyze sources
    const tonePrefix = getTonePrefix(tone)
    let responseContent = openaiData.choices[0]?.message?.content || 'No se pudo generar respuesta'
    
    // Check if user explicitly requested a product card
    const userRequestedCard = message.toLowerCase().includes('tarjeta') || 
                             message.toLowerCase().includes('muestra') ||
                             message.toLowerCase().includes('crea') ||
                             message.toLowerCase().includes('visual') ||
                             message.toLowerCase().includes('card') ||
                             message.toLowerCase().includes('tarjeta simple') ||
                             message.toLowerCase().includes('tarjeta peque√±a')
    
    // AUTOM√ÅTICO: Siempre generar tarjeta cuando se encuentre un producto
    if (products && products.length > 0) {
      // Buscar producto espec√≠fico mencionado en el mensaje
      const messageLower = message.toLowerCase()
      
      // Primero buscar coincidencia exacta del nombre del producto
      let mentionedProduct = products.find(p => {
        const productName = p.name.toLowerCase()
        return messageLower.includes(productName)
      })
      
      // Si no encuentra coincidencia exacta, buscar por palabras clave
      if (!mentionedProduct) {
        // Extraer palabras clave del mensaje (excluyendo palabras comunes)
        const commonWords = ['una', 'de', 'la', 'el', 'para', 'con', 'tarjeta', 'simple', 'muestra', 'crea', 'busco', 'quiero', 'necesito']
        const messageWords = messageLower.split(' ').filter(word => 
          word.length > 2 && !commonWords.includes(word)
        )
        
        mentionedProduct = products.find(p => {
          const productName = p.name.toLowerCase()
          return messageWords.some(word => productName.includes(word))
        })
      }
      
      if (mentionedProduct) {
        console.log('üéØ PRODUCTO ENCONTRADO PARA TARJETA AUTOM√ÅTICA:', mentionedProduct.name)
        const productCard = generateSimpleProductCard(mentionedProduct)
        // Insert product card at the beginning of the response
        responseContent = productCard + '\n\n' + responseContent
      } else {
        console.log('‚ùå NO SE ENCONTR√ì PRODUCTO PARA TARJETA AUTOM√ÅTICA en mensaje:', message)
      }
    }
    
    // Analyze response content to determine sources
    const sources = []
    
    // Check if response mentions specific products from catalog
    if (products && products.some(p => {
      const productName = p.name.toLowerCase()
      const responseLower = responseContent.toLowerCase()
      return responseLower.includes(productName) || 
             productName.split(' ').some(word => word.length > 3 && responseLower.includes(word))
    })) {
      sources.push("Cat√°logo de productos")
    }
    
    // Check if response mentions documentation content
    if (docs && docs.some(doc => {
      const docName = doc.name.toLowerCase()
      const docContent = doc.content.toLowerCase()
      const responseLower = responseContent.toLowerCase()
      
      return responseLower.includes(docName) || 
             docContent.split(' ').some(word => 
               word.length > 4 && responseLower.includes(word) && 
               !['producto', 'precio', 'categor√≠a', 'descripci√≥n'].includes(word)
             )
    })) {
      sources.push("Documentaci√≥n interna")
    }
    
    // Check if response contains web-like information
    const webIndicators = [
      'http', 'www.', 'precio de mercado', 'actualizado', 'seg√∫n', 'fuente externa',
      'investigaci√≥n', 'estudio', 'tendencia', 'an√°lisis', 'comparativa'
    ]
    
    if (webIndicators.some(indicator => responseContent.toLowerCase().includes(indicator))) {
      sources.push("Informaci√≥n web")
    }
    
    // Check if response contains product-specific information (prices, SKUs, etc.)
    if (responseContent.includes('‚Ç¨') || responseContent.includes('SKU') || 
        responseContent.includes('stock') || responseContent.includes('categor√≠a')) {
      if (!sources.includes("Cat√°logo de productos")) {
        sources.push("Cat√°logo de productos")
      }
    }
    
    // If no specific sources detected, assume general knowledge
    if (sources.length === 0) {
      sources.push("Conocimiento general")
    }
    
    // Create specific disclaimer based on detected sources
    const disclaimer = `üìã Fuentes de esta respuesta: ${sources.join(', ')}`
    
    // Ensure disclaimer is always included
    if (!responseContent.includes("üìã Fuentes de esta respuesta")) {
      responseContent += `\n\n${disclaimer}`
    }
    
    const finalAnswer = `${tonePrefix} ${responseContent}`

    return new Response(
      JSON.stringify({
        answer: finalAnswer,
        usage: openaiData.usage,
        sources: {
          catalog: products ? products.length : 0,
          documentation: docs ? docs.length : 0,
          web: true
        }
      }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    )

  } catch (error) {
    console.error('Error in OpenAI chat function:', error)
    return new Response(
      JSON.stringify({ 
        error: `Unexpected error: ${error instanceof Error ? error.message : 'Unknown error'}` 
      }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    )
  }
})

// Helper function to apply tone prefix
function getTonePrefix(tone: string): string {
  switch (tone) {
    case 'premium':
      return '‚ú®'
    case 'technical':
      return 'üîß'
    case 'casual':
      return 'üòä'
    case 'professional':
      return 'üìã'
    case 'friendly':
    default:
      return 'üëã'
  }
}

// Helper function to generate simple product card HTML
function generateSimpleProductCard(product: Product): string {
  // Crear tarjeta simple y elegante que siga el estilo de la web
  const imageHtml = product.image_url 
    ? `<img src="${product.image_url}" alt="${product.name}" style="width: 120px; height: 120px; object-fit: cover; border-radius: 8px; margin: 0 auto 12px; display: block;">`
    : `<div style="width: 120px; height: 120px; background: linear-gradient(135deg, #f8f9fa 0%, #e9ecef 100%); border-radius: 8px; margin: 0 auto 12px; display: flex; align-items: center; justify-content: center; color: #6c757d; font-size: 24px;">üì¶</div>`
  
  // Bot√≥n simple y elegante
  const buyButtonHtml = product.external_id 
    ? `<a href="${product.external_id}" target="_blank" style="background: #007bff; color: white; padding: 8px 16px; text-decoration: none; border-radius: 6px; display: inline-flex; align-items: center; gap: 6px; font-size: 14px; font-weight: 500; transition: background-color 0.2s;">
        <span>üõí</span> Comprar
      </a>`
    : `<a href="https://100x100chef.com/" target="_blank" style="background: #28a745; color: white; padding: 8px 16px; text-decoration: none; border-radius: 6px; display: inline-flex; align-items: center; gap: 6px; font-size: 14px; font-weight: 500;">
        <span>üõí</span> Ver en Tienda
      </a>`
  
  return `
<div style="background: white; border: 1px solid #e9ecef; border-radius: 12px; padding: 20px; margin: 16px 0; box-shadow: 0 2px 8px rgba(0,0,0,0.08); max-width: 280px; font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;">
  <h3 style="margin: 0 0 12px 0; font-size: 16px; font-weight: 600; color: #212529; line-height: 1.3;">${product.name}</h3>
  ${imageHtml}
  <div style="text-align: center; margin-bottom: 12px;">
    <span style="font-size: 20px; font-weight: 700; color: #007bff;">${product.price}‚Ç¨</span>
  </div>
  <p style="margin: 0 0 12px 0; font-size: 13px; color: #6c757d; line-height: 1.4;">${product.description || 'Sin descripci√≥n'}</p>
  <div style="text-align: center;">
    ${buyButtonHtml}
  </div>
</div>`
}
